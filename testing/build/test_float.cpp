#include <iostream>
#include <string>

int main() {
    std::cout << "123.5f -> " << std::to_string(123.5f) << std::endl;
    std::cout << "123.0f -> " << std::to_string(123.0f) << std::endl;
    std::cout << "123.4f -> " << std::to_string(123.4f) << std::endl;
    std::cout << "100.0f -> " << std::to_string(100.0f) << std::endl;
    std::cout << "1.0f -> " << std::to_string(1.0f) << std::endl;
    return 0;
}
